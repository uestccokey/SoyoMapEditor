    private void infoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_infoButtonActionPerformed
        // TODO add your handling code here:
        if (!plugins.isEmpty()) {
            if (pid == null) {
                pid = new PluginInfoDialog();
            }
            pid.pluginInfoTextArea.setText(plugins.get(curRow).getDescription());
            pid.setVisible(true);
        }
    }//GEN-LAST:event_infoButtonActionPerformed
    private int curRow = -1;
    private PluginInfoDialog pid;
    private void pluginTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pluginTableMouseClicked
        // TODO add your handling code here:
        curRow = pluginTable.getSelectedRow();
        if (!plugins.isEmpty() && evt.getClickCount() >= 2) {
            if (pid == null) {
                pid = new PluginInfoDialog();
            }
            pid.pluginInfoTextArea.setText(plugins.get(curRow).getDescription());
            pid.setVisible(true);
        }
    }//GEN-LAST:event_pluginTableMouseClicked

