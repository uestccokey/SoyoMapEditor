    private void initPluginTable() {
        try {
            plugins = PluginLoader.getInstance().getPlugins();
            for (int i = 0, n = plugins.size(); i < n; i++) {
                String[] data = {plugins.get(i).getName(), plugins.get(i).getAuthor(), plugins.get(i).getVersion()};
                pluginTableModel.addRow(data);
            }
        } catch (Exception e) {
            Log.getLogger(this.getClass()).error("初始化插件信息异常！", e);
        }

    }
